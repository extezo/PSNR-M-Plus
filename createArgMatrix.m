function X = createArgMatrix(vec1, vec2, vec3)

N = length(vec1);
X = [ones(N,1) vec1 vec2 vec3...
    vec1.^2 vec2.^2 vec3.^2 vec1.* vec2 vec1.* vec3 vec2.* vec3...
    vec1.^3 vec2.^3 vec3.^3 vec1.^2 .*vec2 vec1.^2 .*vec3 ...
    vec2.^2 .*vec1 vec2.^2 .*vec3 vec3.^2 .*vec1 vec3.^2 .*vec2...
    vec1 .* vec2 .* vec3 ...
    vec1.^4 vec2.^4 vec3.^4 vec1.^3 .* vec2 vec1.^3 .* vec3...
    vec2.^3 .* vec1 vec2.^3 .* vec3 vec3.^3 .* vec1 vec3.^3 .* vec2 ...
    vec1.^2 .* vec2.^2 vec1.^2 .* vec3.^2 vec2.^2 .* vec3.^2 ...
    vec1.^2 .* vec2 .* vec3 vec2.^2 .* vec1 .* vec3 vec3.^2 .* vec1 .* vec2];

end

